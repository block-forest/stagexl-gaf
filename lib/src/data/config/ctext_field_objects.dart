 part of stagexl_gaf;
	/**
	 * @
	 */
	 class CTextFieldObjects
	{
		//--------------------------------------------------------------------------
		//
		//  PUBLIC VARIABLES
		//
		//--------------------------------------------------------------------------

		//--------------------------------------------------------------------------
		//
		//  PRIVATE VARIABLES
		//
		//--------------------------------------------------------------------------

		 Map _textFieldObjectsMap;

		//--------------------------------------------------------------------------
		//
		//  CONSTRUCTOR
		//
		//--------------------------------------------------------------------------
	 CTextFieldObjects()
		{
			_textFieldObjectsMap = {};
		}

		//--------------------------------------------------------------------------
		//
		//  PUBLIC METHODS
		//
		//--------------------------------------------------------------------------

		  void addTextFieldObject(CTextFieldObject textFieldObject)
		{
			if (!this._textFieldObjectsMap[textFieldObject.id])
			{
				this._textFieldObjectsMap[textFieldObject.id] = textFieldObject;
			}
		}

		  CAnimationObject getAnimationObject(String id)
		{
			if (this._textFieldObjectsMap[id])
			{
				return this._textFieldObjectsMap[id];
			}
			else
			{
				return null;
			}
		}

		//--------------------------------------------------------------------------
		//
		//  PRIVATE METHODS
		//
		//--------------------------------------------------------------------------

		//--------------------------------------------------------------------------
		//
		// OVERRIDDEN METHODS
		//
		//--------------------------------------------------------------------------

		//--------------------------------------------------------------------------
		//
		//  EVENT HANDLERS
		//
		//--------------------------------------------------------------------------

		//--------------------------------------------------------------------------
		//
		//  GETTERS AND SETTERS
		//
		//--------------------------------------------------------------------------

		  Map get textFieldObjectsMap
		{
			return this._textFieldObjectsMap;
		}

		//--------------------------------------------------------------------------
		//
		//  STATIC METHODS
		//
		//--------------------------------------------------------------------------

	}
