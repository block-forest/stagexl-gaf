 part of stagexl_gaf;



	/**
	 * @
	 */
	 class TAGFXSourceBitmapData extends TAGFXBase
	{
		//--------------------------------------------------------------------------
		//
		//  PUBLIC VARIABLES
		//
		//--------------------------------------------------------------------------

		//--------------------------------------------------------------------------
		//
		//  PRIVATE VARIABLES
		//
		//--------------------------------------------------------------------------

		//--------------------------------------------------------------------------
		//
		//  CONSTRUCTOR
		//
		//--------------------------------------------------------------------------
	 TAGFXSourceBitmapData(BitmapData source,String format)
		{
			this._source = source;
			this._textureFormat = format;
		}

		//--------------------------------------------------------------------------
		//
		//  PUBLIC METHODS
		//
		//--------------------------------------------------------------------------

		//--------------------------------------------------------------------------
		//
		//  PRIVATE METHODS
		//
		//--------------------------------------------------------------------------

		//--------------------------------------------------------------------------
		//
		// OVERRIDDEN METHODS
		//
		//--------------------------------------------------------------------------

		@override 
		  String get sourceType
		{
			return SOURCE_TYPE_BITMAP_DATA;
		}

		@override 
		  Texture get texture
		{
			if (!this._texture)
			{
				this._texture = Texture.fromBitmapData(this._source, GAF.useMipMaps, false, this._textureScale, this._textureFormat);
				if (this._clearSourceAfterTextureCreated)
					(this._source as BitmapData).dispose();
			}

			return this._texture;
		}

		//--------------------------------------------------------------------------
		//
		//  EVENT HANDLERS
		//
		//--------------------------------------------------------------------------

		//--------------------------------------------------------------------------
		//
		//  GETTERS AND SETTERS
		//
		//--------------------------------------------------------------------------

		//--------------------------------------------------------------------------
		//
		//  STATIC METHODS
		//
		//--------------------------------------------------------------------------
	}
